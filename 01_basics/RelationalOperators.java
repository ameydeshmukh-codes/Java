public class RelationalOperators {
    public static void main(String[] args) {
        int x = 15;
        int y = 10;

        // Equal to
        System.out.println("x == y: " + (x == y));

        // Not equal to
        System.out.println("x != y: " + (x != y));

        // Greater than
        System.out.println("x > y: " + (x > y));

        // Less than
        System.out.println("x < y: " + (x < y));

        // Greater than or equal to
        System.out.println("x >= y: " + (x >= y));

        // Less than or equal to
        System.out.println("x <= y: " + (x <= y));
    }
}
